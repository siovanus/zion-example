func init() {
	// Initialize the CLI app and start Geth
	app.Action = geth
	app.HideVersion = true // we have a command to print the version
	app.Copyright = "Copyright 2013-2021 The go-ethereum Authors"
	app.Commands = []cli.Command{
		// See chaincmd.go:
		sub.InitCommand,
		sub.ImportCommand,
		sub.ExportCommand,
		sub.ImportPreimagesCommand,
		sub.ExportPreimagesCommand,
		sub.RemovedbCommand,
		sub.DumpCommand,
		sub.DumpGenesisCommand,
		// See accountcmd.go:
		sub.AccountCommand,
		sub.WalletCommand,
		// See consolecmd.go:
		consoleCommand,
		attachCommand,
		javascriptCommand,
		// See misccmd.go:
		sub.MakecacheCommand,
		sub.MakedagCommand,
		sub.VersionCommand,
		sub.VersionCheckCommand,
		sub.LicenseCommand,
		// See config.go
		sub.NewDumpConfigCommand(nodeFlags, rpcFlags),
		// see dbcmd.go
		sub.DbCommand,
		// See cmd/utils/flags_legacy.go
		utils.ShowDeprecated,
		// See snapshot.go
		sub.SnapshotCommand,
	}
	sort.Sort(cli.CommandsByName(app.Commands))

	app.Flags = append(app.Flags, nodeFlags...)
	app.Flags = append(app.Flags, rpcFlags...)
	app.Flags = append(app.Flags, consoleFlags...)
	app.Flags = append(app.Flags, debug.Flags...)
	app.Flags = append(app.Flags, metricsFlags...)

	app.Before = func(ctx *cli.Context) error {
		return debug.Setup(ctx)
	}
	app.After = func(ctx *cli.Context) error {
		debug.Exit()
		prompt.Stdin.Close() // Resets terminal mode.
		return nil
	}
}

func main() {
	if err := app.Run(os.Args); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

// geth is the main entry point into the system if no special subcommand is ran.
// It creates a default node based on the command line arguments and runs it in
// blocking mode, waiting for it to be shut down.
func geth(ctx *cli.Context) error {
	if args := ctx.Args(); len(args) > 0 {
		return fmt.Errorf("invalid command: %q", args[0])
	}

	sub.Prepare(ctx)
	stack, backend := sub.MakeFullNode(ctx, boot.InitModuleContracts)
	defer stack.Close()

	sub.StartNode(ctx, stack, backend)
	stack.Wait()
	return nil
}
